// Fungsi Loop for pertama saya

// loop ini akan membuat angka deret aritmatika dari 10 - 100 dengan dikurangi 10 tiap iterasi
for ($i = 10; $i <= 100; $i += 10) {
  echo $i . "\n"; // "\n" seperti <br> di html untuk menampilkan nilai loop $i memanjang kebawah 
};

// hitung mundur dari 50 - 5 dikurangi 5 tiap iterasi
for ($i = 50; $i >= 5; $i -= 5) {
  echo $i . "\n";
};